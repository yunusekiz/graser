İskeletli Yapı Sistemi olacaktır.</p><p class="MsoNormal" style="text-align:justify"><b><u><br></u></b></p><p class="MsoNormal" style="text-align:justify"><b><u>DEPREM GÜVENLİĞİ<o:p></o:p></u></b></p><p class="MsoNormal" style="text-align:justify">Depremde
binalara nüfuz eden kuvvetler binanın ağırlığıyla doğru orantılı olarak etki
