kayıt ve dikmelerin üstüne konulan adedi hesaplarla belirlenmiş ahşap üst
kayıtlardan oluşmaktadır. Her bir dikme alt ve üst kayıtlara 2’şer adet ahşap
vidası ile birleştirilmektedir. Kapı-pencere boşluklarında üzerindeki
